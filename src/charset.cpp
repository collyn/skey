/// Encode a byte as its Latin-1 UTF-8 representation.
static void appendByte(std::string &result, uint8_t b) {
    if (b <= 0x7F) {
        result += static_cast<char>(b);
    } else {
        // Latin-1 codepoint = byte value → UTF-8 2-byte sequence
        result += static_cast<char>(0xC0 | (b >> 6));
        result += static_cast<char>(0x80 | (b & 0x3F));
    }
}

/// Decode a single UTF-8 codepoint, advancing the position.
static uint32_t decodeUtf8(const std::string &s, size_t &i) {
    if (i >= s.size()) return 0;
    unsigned char c = static_cast<unsigned char>(s[i]);
    uint32_t cp;
    int len;

    if (c <= 0x7F)       { cp = c; len = 1; }
    else if ((c & 0xE0) == 0xC0) { cp = c & 0x1F; len = 2; }
    else if ((c & 0xF0) == 0xE0) { cp = c & 0x0F; len = 3; }
    else if ((c & 0xF8) == 0xF0) { cp = c & 0x07; len = 4; }
    else { i++; return 0; }

    for (int j = 1; j < len && i + j < s.size(); j++) {
        cp = (cp << 6) | (static_cast<unsigned char>(s[i + j]) & 0x3F);
    }
    i += len;
    return cp;
}

// =============================================================================
// Main conversion function
// =============================================================================

std::string convertCharset(const std::string &utf8, Charset cs) {
    if (cs == Charset::Unicode) return utf8;
    if (utf8.empty()) return utf8;

    const CharMap *map = (cs == Charset::TCVN3) ? &kTcvn3 : &kVni;

    std::string result;
    result.reserve(utf8.size() * 2); // worst-case: each char → 2-byte Latin-1

    for (size_t i = 0; i < utf8.size();) {
        unsigned char c = static_cast<unsigned char>(utf8[i]);

        // ASCII — pass through unchanged
        if (c <= 0x7F) {
            result += static_cast<char>(c);
            i++;
            continue;
        }

        // Decode the UTF-8 codepoint
        size_t saved = i;
        uint32_t cp = decodeUtf8(utf8, i);

        // Look up in charset table
        auto it = map->find(cp);
        if (it != map->end()) {
            for (uint8_t b : it->second) {
                appendByte(result, b);
            }
        } else {
            // Unknown — fall back to original UTF-8 bytes
            for (size_t j = saved; j < i && j < utf8.size(); j++) {
                result += utf8[j];
            }
        }
    }

    return result;
}

